package midtermgitcode2.midtermgitcode2;

/**
 *
 * @author krish
 */
public class Student {
    
    private String name;
    private int number;
    
    public Student(String name, int number) {
        this.name = name;
        this.number = number;
    }
    
    public String getName() {
        return this.name;
    }
    
    public int getNumber() {
        return this.number;
    }
}
